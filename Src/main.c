#define CRC16_INIT 0xFFFF
uint16_t CRC16(const uint8_t *pcBlock, uint32_t len, uint16_t crc)
{
	uint8_t ixx;
	while( len-- )
	{
		crc ^= *pcBlock++ << 8;  
		for( ixx = 0; ixx < 8; ixx++ )
		{
			crc = crc & 0x8000 ? ( crc << 1 ) ^ 0x1021 : crc << 1; 
		}
	}
	return crc;
}



